def _analytics_summary() -> dict[str, Any]:
    events = read_audit_events(limit=500)
    decisions = [event for event in events if event.get("event_type") == "classification_decision"]
    appeals = [event for event in events if event.get("event_type") == "appeal_received"]
    certificates = read_certificates(limit=500)

    by_classification: dict[str, int] = {}
    by_content_type: dict[str, int] = {}
    confidence_total = 0.0
    for decision in decisions:
        classification = str(decision.get("classification") or "unknown")
        content_type = str(decision.get("content_type") or "text")
        by_classification[classification] = by_classification.get(classification, 0) + 1
        by_content_type[content_type] = by_content_type.get(content_type, 0) + 1
        confidence_total += float(decision.get("confidence") or 0)

    uncertain_count = by_classification.get("uncertain", 0)
    total_decisions = len(decisions)
    appeal_rate = round(len(appeals) / total_decisions, 3) if total_decisions else 0.0
    uncertain_rate = round(uncertain_count / total_decisions, 3) if total_decisions else 0.0
    average_confidence = round(confidence_total / total_decisions, 3) if total_decisions else 0.0

    return {
        "summary": {
            "total_decisions": total_decisions,
            "open_appeals": len(appeals),
            "appeal_rate": appeal_rate,
            "uncertain_rate": uncertain_rate,
            "average_confidence": average_confidence,
            "verified_creators": len(certificates),
        },
        "by_classification": by_classification,
        "by_content_type": by_content_type,
        "recent_decisions": decisions[-6:],
        "recent_appeals": appeals[-6:],
    }


def _legacy_attribution(classification: str) -> str:
    return {
        "likely_ai_generated": "likely_ai",
        "likely_human_written": "likely_human",
        "uncertain": "uncertain",
    }.get(classification, "uncertain")


def _find_audit_decision(content_id: str) -> dict[str, Any] | None:
    for event in reversed(read_audit_events(limit=250)):
        if event.get("event_type") == "classification_decision" and event.get("content_id") == content_id:
            return event
    return None


