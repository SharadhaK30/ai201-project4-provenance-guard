def _safe_limit(raw_value: str | None) -> int:
    try:
        return max(1, min(250, int(raw_value or "50")))
    except ValueError:
        return 50


def _validate_content(content: str, field_name: str = "content") -> Any | None:
    if not content:
        return jsonify({"error": "validation_error", "message": f"`{field_name}` is required."}), 400
    if len(content) > MAX_CONTENT_CHARS:
        return jsonify(
            {
                "error": "validation_error",
                "message": f"`{field_name}` must be {MAX_CONTENT_CHARS} characters or less.",
            }
        ), 400
    return None


